
public class FoodItem<T> {
	private String name;
	private int cal;
	private int carb;
	private int pro;
	private int fat;
	private int fib;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCal() {
		return cal;
	}
	public void setCal(int cal) {
		this.cal = cal;
	}
	public int getCarb() {
		return carb;
	}
	public void setCarb(int carb) {
		this.carb = carb;
	}
	public int getPro() {
		return pro;
	}
	public void setPro(int pro) {
		this.pro = pro;
	}
	public int getFat() {
		return fat;
	}
	public void setFat(int fat) {
		this.fat = fat;
	}
	public int getFib() {
		return fib;
	}
	public void setFib(int fib) {
		this.fib = fib;
	}
	
}
